public class MathDemo4{
	public static void main(String args[]){
		System.out.println("Ceil of 5.6 is "+Math.ceil(5.6));
		System.out.println("Ceil of 5.2 is "+Math.ceil(5.2));
		System.out.println("Floor of 6.2 is "+Math.floor(6.2));
		System.out.println("Floor of 6.8 is "+Math.floor(6.8));
		System.out.println("Round of 7.4 is "+Math.round(7.4));
		System.out.println("Round of 7.9 is "+Math.round(7.9));
		System.out.println("6 ^ 3 is "+Math.pow(6,3));
	}
}
